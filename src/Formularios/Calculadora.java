//MÃ©todo con las operaciones de suma resta multiplicaciÃ³n y divisiÃ³n
    private void Operaciones(double valor1, double valor2){
        //Switch que evalÃºa la variable tipoOperacines
        switch(tipoOperaciones){
            //Case con las funcionese de suma
            case "+":
                resultado = valor1 + valor2;
                TextField_Principal.setText(resultado + " ");
                valor1 = Double.parseDouble(TextField_Principal.getText());
                break;
                //Case con las funcionese de resta
            case "-":
                resultado = valor1 - valor2;
                TextField_Principal.setText(resultado + " ");
                valor1 = Double.parseDouble(TextField_Principal.getText());
                break;
                //Case con las funcionese de multiplicar
            case "*":
                resultado = valor1 * valor2;
                TextField_Principal.setText(resultado + " ");
                valor1 = Double.parseDouble(TextField_Principal.getText());
                break;
                //Case con las funcionese de suma
            case "/":
                if (valor2 == 0) {
                    TextField_Principal.setText("Error ");
                    break;
                 }else{
                    resultado = valor1 / valor2;
                    TextField_Principal.setText(resultado + " ");
                    valor1 = Double.parseDouble(TextField_Principal.getText());
                    break;
              }
            //Case de la funciÃ³n que saca la potencia de un nÃºmero
            case "potencia":
                resultado = Math.pow(valor1,valor2);
               TextField_Principal.setText(resultado +" ");
                break;
                
              //Case de la funciÃ³n que saca la raÃ­z x de un nÃºmero
            case "raizx":
                if (valor2 <=0) {
                TextField_Principal.setText("Error ");
                break;
            }else{
                TextField_Previo.setText(valor2 +" yroot ");
                a = 1;
                c = a/valor2;
                TextField_Principal.setText(Math.pow(valor1, c) + " ");
                break;
            }
        }
    }

